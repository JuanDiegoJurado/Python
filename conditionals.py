
#conditional IF
x = 30

if x < 30: 
    print("x es menor que 30")
elif x == 30:
    print("x es igual a 30")
else:
    print("x es mayor que 30")
    
#if and/or
if x > 2 and x < 10:
    print("x se encuentra entre 2 y 10")
else:    
    print("x es menor a 2 o mayor a 10")
    
#not
if(not(x == 30)):
    print("x no es igual a 30")
else:
    print("x es igual a 30")
    
    
    
    



